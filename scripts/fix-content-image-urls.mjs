/**
 * Fixes relative image paths in post content by replacing them with full Strapi URLs.
 *
 * The content has paths like: ![alt](Aesthetics Clothing/gothic-dress-2.webp)
 * The storage-url-map.json has keys like: blog-images/Aesthetics Clothing/gothic-dress-2.webp
 *
 * We strip the leading bucket prefix (blog-images/) to match against content paths.
 */
import { readFileSync } from 'fs';

const STRAPI_URL = process.env.STRAPI_URL || 'https://strapi.zenmanager.eu';
const STRAPI_API_TOKEN = process.env.STRAPI_API_TOKEN || 'missing_token';
const URL_MAP_FILE = process.env.URL_MAP_FILE || './storage-url-map.json';
const BUCKET_PREFIX = 'blog-images/';

async function main() {
  // Load the URL map generated by upload-storage-to-strapi.mjs
  const urlMap = JSON.parse(readFileSync(URL_MAP_FILE, 'utf8'));

  // Build a map: relative path (without bucket) ‚Üí full Strapi URL
  const relativeToStrapi = {};
  for (const [key, val] of Object.entries(urlMap)) {
    const relativePath = key.startsWith(BUCKET_PREFIX) ? key.slice(BUCKET_PREFIX.length) : key;
    const fullUrl = val.startsWith('http') ? val : `${STRAPI_URL}${val}`;
    relativeToStrapi[relativePath] = fullUrl;
  }

  console.log(`Built map with ${Object.keys(relativeToStrapi).length} entries`);

  // Fetch all posts
  const res = await fetch(`${STRAPI_URL}/api/posts?pagination[pageSize]=100&status=published`, {
    headers: { 'Authorization': `Bearer ${STRAPI_API_TOKEN}` },
  });
  const { data: posts } = await res.json();
  console.log(`Fetched ${posts.length} posts\n`);

  let updatedCount = 0;

  for (const post of posts) {
    let content = post.content || '';
    let changed = false;

    // Replace markdown image refs: ![alt](relative/path.webp) ‚Üí ![alt](https://strapi.../uploads/...)
    content = content.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, (match, alt, src) => {
      // Skip already absolute URLs
      if (src.startsWith('http://') || src.startsWith('https://')) return match;

      const strapiUrl = relativeToStrapi[src];
      if (strapiUrl) {
        changed = true;
        return `![${alt}](${strapiUrl})`;
      }
      console.warn(`  ‚öÝÔ∏è  No mapping found for: ${src}`);
      return match;
    });

    if (changed) {
      const updateRes = await fetch(`${STRAPI_URL}/api/posts/${post.documentId}`, {
        method: 'PUT',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${STRAPI_API_TOKEN}`,
        },
        body: JSON.stringify({ data: { content } }),
      });

      if (updateRes.ok) {
        updatedCount++;
        console.log(`  ‚úÖ Updated: ${post.slug}`);
      } else {
        const err = await updateRes.text();
        console.error(`  ‚ùå Failed to update ${post.slug}: ${err.substring(0, 200)}`);
      }
    } else {
      console.log(`  ‚îÄ No changes: ${post.slug}`);
    }
  }

  console.log(`\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ`);
  console.log(`Updated ${updatedCount} / ${posts.length} posts`);
}

main().catch(err => { console.error(err); process.exit(1); });
